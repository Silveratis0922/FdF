#include "fdf.h"

int	verif_arg(char	*str, char *verif)
{
	int	i;
	int	j;

	i = 0;
	j = 0;
	while (str[i])
	{
		if (str[i] == verif[j])
			j++;
		else
			j = 0;
		i++;
	}
	if (j == 4)
		return (0);
	return (1);
}
